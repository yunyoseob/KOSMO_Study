package a.b.c.ch1;

// 자바는 UTF-16 코드를 지원한다. 한글을 지원한다.
public class 윤요섭{
	public static void main(String args[]){
		System.out.println("윤요섭 소개 시작 ");
		// 자바 공부하는 윤요섭
		// 자료구조와 알고리즘 공부하는 윤요섭
		// 데이터 분석 공부하는 윤요섭

		윤요섭 자바=new 윤요섭();
		System.out.println("자바 >>> : "+자바);
		윤요섭 자료구조와알고리즘=new 윤요섭();
		System.out.println("자료구조와알고리즘 >>> : "+자료구조와알고리즘);
		윤요섭 데이터분석=new 윤요섭();
		System.out.println("데이터분석 >>> : "+데이터분석);
	}
} // end of 윤요섭 클래스